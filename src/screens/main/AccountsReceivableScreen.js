import React, { useState, useCallback, useMemo } from "react";
import {
  View,
  Text,
  StyleSheet,
  FlatList,
  TouchableOpacity,
  Alert,
  TextInput,
  ActivityIndicator,
} from "react-native";
import { useFocusEffect } from "@react-navigation/native";
import { useAccounts } from "../../hooks/useAccounts";
import { formatCurrency } from "../../utils/currency";

export const AccountsReceivableScreen = ({ navigation }) => {
  const {
    accountsReceivable,
    loading,
    error,
    refresh,
    searchReceivable,
    removeAccountReceivable,
    markReceivableAsPaid,
    receivableStats,
  } = useAccounts();

  const [searchQuery, setSearchQuery] = useState("");
  const [activeTab, setActiveTab] = useState("pending");

  const handleSearch = useCallback(
    async (query) => {
      setSearchQuery(query);
      if (!query.trim()) {
        await refresh();
      } else {
        await searchReceivable(query);
      }
    },
    [refresh, searchReceivable]
  );

  // Filtrar cuentas basado en el tab activo
  const filteredAccounts = useMemo(() => {
    if (activeTab === "pending") {
      return accountsReceivable.filter((account) => account.status !== "paid");
    } else if (activeTab === "paid") {
      return accountsReceivable.filter((account) => account.status === "paid");
    }
    return accountsReceivable;
  }, [accountsReceivable, activeTab]);

  const openAddScreen = useCallback(() => {
    navigation.navigate("AddAccountReceivable");
  }, [navigation]);

  const openEditScreen = useCallback(
    (account) => {
      navigation.navigate("EditAccountReceivable", { account });
    },
    [navigation]
  );

  const handleMarkAsPaid = useCallback(
    (account) => {
      Alert.alert(
        "Marcar como pagada",
        `¬øConfirmar que la cuenta de ${
          account.customerName
        } por ${formatCurrency(account.amount || 0, "VES")} ha sido pagada?`,
        [
          { text: "Cancelar", style: "cancel" },
          {
            text: "Confirmar",
            onPress: async () => {
              try {
                await markReceivableAsPaid(account.id);
                Alert.alert("√âxito", "Cuenta marcada como pagada");
              } catch (err) {
                Alert.alert("Error", "No se pudo actualizar la cuenta");
              }
            },
          },
        ]
      );
    },
    [markReceivableAsPaid]
  );

  const handleDelete = useCallback(
    (account) => {
      Alert.alert(
        "Eliminar cuenta",
        `¬øEliminar la cuenta de ${account.customerName}?`,
        [
          { text: "Cancelar", style: "cancel" },
          {
            text: "Eliminar",
            style: "destructive",
            onPress: async () => {
              try {
                await removeAccountReceivable(account.id);
                Alert.alert("√âxito", "Cuenta eliminada correctamente");
              } catch (err) {
                Alert.alert("Error", "No se pudo eliminar la cuenta");
              }
            },
          },
        ]
      );
    },
    [removeAccountReceivable]
  );

  const totalAmount = receivableStats?.totalAmount || 0;
  const totalCount =
    typeof receivableStats?.total === "number"
      ? receivableStats.total
      : accountsReceivable.length;

  const getStatusAppearance = useCallback((status, dueDate) => {
    if (status === "paid") {
      return {
        label: "Pagada",
        backgroundColor: "#e5f7ed",
        color: "#2e7d32",
      };
    }

    if (dueDate && new Date(dueDate) < new Date()) {
      return {
        label: "Vencida",
        backgroundColor: "#fdecea",
        color: "#c62828",
      };
    }

    return {
      label: "Pendiente",
      backgroundColor: "#fff4e5",
      color: "#ef6c00",
    };
  }, []);

  const renderAccount = useCallback(
    ({ item }) => {
      const appearance = getStatusAppearance(item.status, item.dueDate);

      return (
        <TouchableOpacity
          style={styles.accountCard}
          activeOpacity={item.invoiceNumber ? 1 : 0.9}
          onPress={item.invoiceNumber ? undefined : () => openEditScreen(item)}
        >
          <View style={styles.cardHeader}>
            <Text style={styles.customerName}>{item.customerName}</Text>
            <View style={styles.statusContainer}>
              {item.invoiceNumber && (
                <View style={styles.autoGeneratedBadge}>
                  <Text style={styles.autoGeneratedText}>Auto</Text>
                </View>
              )}
              <View
                style={[
                  styles.statusBadge,
                  { backgroundColor: appearance.backgroundColor },
                ]}
              >
                <Text style={[styles.statusText, { color: appearance.color }]}>
                  {appearance.label}
                </Text>
              </View>
            </View>
          </View>

          <View style={styles.amountRow}>
            <Text style={styles.amount}>
              {formatCurrency(item.amount || 0, item.currency || "VES")}
            </Text>
            {item.dueDate ? (
              <Text style={styles.dueDate}>
                Vence {new Date(item.dueDate).toLocaleDateString()}
              </Text>
            ) : null}
          </View>

          {item.invoiceNumber ? (
            <Text style={styles.invoiceNumber}>
              Factura {item.invoiceNumber}
            </Text>
          ) : null}
          {item.documentNumber ? (
            <Text style={styles.metaText}>C√©dula: {item.documentNumber}</Text>
          ) : null}
          {item.description ? (
            <Text style={styles.description}>{item.description}</Text>
          ) : null}
          {item.createdAt ? (
            <Text style={styles.metaText}>
              Registrada {new Date(item.createdAt).toLocaleDateString()}
            </Text>
          ) : null}

          <View style={styles.cardFooter}>
            {item.status !== "paid" ? (
              <TouchableOpacity
                style={styles.secondaryButton}
                onPress={() => handleMarkAsPaid(item)}
              >
                <Text style={styles.secondaryButtonText}>Registrar pago</Text>
              </TouchableOpacity>
            ) : null}

            <TouchableOpacity
              style={[
                styles.iconButton,
                item.status === "paid" && styles.iconButtonAlone,
              ]}
              onPress={() => handleDelete(item)}
            >
              <Text style={styles.iconButtonText}>üóëÔ∏è</Text>
            </TouchableOpacity>
          </View>
        </TouchableOpacity>
      );
    },
    [getStatusAppearance, handleDelete, handleMarkAsPaid, openEditScreen]
  );

  const renderHeader = () => (
    <View>
      <View style={styles.summaryCard}>
        <View style={styles.summaryHeader}>
          <View style={styles.summaryIcon}>
            <Text style={styles.summaryIconText}>üì•</Text>
          </View>
          <View>
            <Text style={styles.summaryTitle}>
              Cuentas por Cobrar ({totalCount})
            </Text>
          </View>
        </View>

        <Text style={styles.summaryAmount}>
          {formatCurrency(totalAmount, "VES")}
        </Text>
      </View>

      <View style={styles.controlsCard}>
        <View style={styles.searchWrapper}>
          <Text style={styles.searchIcon}>üîç</Text>
          <TextInput
            style={styles.searchInput}
            placeholder="Buscar cuentas..."
            placeholderTextColor="#9aa6b5"
            value={searchQuery}
            onChangeText={handleSearch}
            returnKeyType="search"
          />
        </View>
      </View>

      <View style={styles.tabGroup}>
        {[
          { key: "pending", label: "Pendientes" },
          { key: "paid", label: "Pagadas" },
        ].map((tab) => {
          const active = activeTab === tab.key;
          return (
            <TouchableOpacity
              key={tab.key}
              style={[styles.tabChip, active && styles.tabChipActive]}
              onPress={() => setActiveTab(tab.key)}
              activeOpacity={0.85}
            >
              <Text
                style={[styles.tabChipText, active && styles.tabChipTextActive]}
              >
                {tab.label}
              </Text>
            </TouchableOpacity>
          );
        })}
      </View>
    </View>
  );

  const renderEmpty = () => (
    <View style={styles.emptyState}>
      <View style={styles.emptyCard}>
        <Text style={styles.emptyIcon}>üóíÔ∏è</Text>
        <Text style={styles.emptyTitle}>
          {searchQuery ? "Sin resultados" : "A√∫n no tienes cuentas"}
        </Text>
        <Text style={styles.emptySubtitle}>
          {searchQuery
            ? "Ajusta los t√©rminos de b√∫squeda para intentarlo de nuevo."
            : "Registra tu primera cuenta por cobrar desde el bot√≥n superior."}
        </Text>
      </View>
    </View>
  );

  useFocusEffect(
    useCallback(() => {
      refresh();
    }, [refresh])
  );

  if (loading) {
    return (
      <View style={styles.centerContainer}>
        <ActivityIndicator size="large" color="#4CAF50" />
        <Text style={styles.loadingText}>Cargando cuentas por cobrar...</Text>
      </View>
    );
  }

  if (error) {
    return (
      <View style={styles.centerContainer}>
        <Text style={styles.errorText}>
          Ocurri√≥ un error al cargar la informaci√≥n.
        </Text>
        <TouchableOpacity style={styles.retryButton} onPress={refresh}>
          <Text style={styles.retryButtonText}>Reintentar</Text>
        </TouchableOpacity>
      </View>
    );
  }

  return (
    <View style={styles.container}>
      <FlatList
        data={filteredAccounts}
        renderItem={renderAccount}
        keyExtractor={(item) => item.id.toString()}
        contentContainerStyle={styles.listContent}
        showsVerticalScrollIndicator={false}
        ListHeaderComponent={renderHeader}
        ListEmptyComponent={renderEmpty}
        refreshing={loading}
        onRefresh={refresh}
      />

      <TouchableOpacity
        style={styles.fab}
        onPress={openAddScreen}
        activeOpacity={0.85}
      >
        <Text style={styles.fabIcon}>+</Text>
      </TouchableOpacity>
    </View>
  );
};

